/* 변수와 상수 */

// 변수
let a = 12;
console.log(a);

a = 999; // 변수는 값 변경 가능
console.log(a);

// 상수
const b = 12;
// b = 999; // 상수는 값 변경 불가능
console.log(b);

// 상수는 언제 사용할까?
// 색상처럼 값이 고정되는 값에서 사용
const COLOR_RED = "#ff0000"; // 빨간색
const COLOR_BLACK = "#000000"; // 검정색

// 상수는 대문자, 변수는 소문자로 표기할 것
// 그래야 글자 모양만 보고 구분 가능